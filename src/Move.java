public class Move {
    private int x;
    private int y;
    private int score;
    public Move(){
        x = -1;
        y = -1;
    }
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
